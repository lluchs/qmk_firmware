static float qwerty_song[][2] = SONG(QWERTY_SOUND);
static float gaming_song[][2] = SONG(ZELDA_PUZZLE);

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case LOWER:
      if (record->event.pressed) {
        layer_on(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        layer_off(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      }
      return false;
    case RAISE:
      if (record->event.pressed) {
        layer_on(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        layer_off(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      }
      return false;
    case SWBASE:
      if (record->event.pressed) {
        if (default_layer_state & (1 << _QWERTY)) {
          default_layer_set(1 << _QWERTY_GAMING);
          PLAY_SONG(gaming_song);
        } else {
          default_layer_set(1 << _QWERTY);
          PLAY_SONG(qwerty_song);
        }
      }
      return false;
    case FUNCTION:
      if (record->event.pressed) {
        layer_on(_FUNCTION);
      } else {
        layer_off(_FUNCTION);
      }
      return false;
    case RAI_GUI:
      if (record->event.pressed) {
        register_code(KC_LGUI);
        layer_on(_RAISE);
      } else {
        unregister_code(KC_LGUI);
        layer_off(_RAISE);
      }
      return false;
  }
  return true;
}
